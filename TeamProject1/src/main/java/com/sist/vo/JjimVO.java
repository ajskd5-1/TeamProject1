package com.sist.vo;
/*
 * NO   NOT NULL NUMBER       
NAME NOT NULL VARCHAR2(20) 
G_ID NOT NULL NUMBER  
 */
public class JjimVO {
	private int no;
	private String name;
	private int g_id;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getG_id() {
		return g_id;
	}
	public void setG_id(int g_id) {
		this.g_id = g_id;
	}
	
	
	

}
